     */
    public function store(Request $request)
    {
        request()->validate([
            'title' => 'required|min:3|max:100',
            'description' => 'required|min:3'
        ]);

        $story = Story::create([
            'title' => request('title'),
            'description' => request('description'),
            'slug' => request(str_slug(request('title'))),
            'user_id' => auth()->id()
